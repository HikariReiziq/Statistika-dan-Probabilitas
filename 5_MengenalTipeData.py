# Integer
angka_bulat = 10
print("Integer:", angka_bulat)
#Integer cocok untuk operasi matematika bilangan bulat.

# Float
angka_desimal = 3.14
print("Float:", angka_desimal)
#Float digunakan untuk menghitung angka dengan desimal.

# String
teks = "Halo, Ular!"
print("String:", teks)
#String berguna untuk teks atau karakter.

# Boolean
benar_salah = True
print("Boolean:", benar_salah)
#Boolean digunakan untuk pengambilan keputusan (logika).

# List
daftar_angka = [1, 2, 3, 4, 5]
print("List:", daftar_angka)
#List untuk menyimpan data yang dapat diubah.

# Tuple
daftar_tetap = (1, 2, 3)
print("Tuple:", daftar_tetap)
#Tuple untuk data yang tidak berubah.

# Dictionary
kamus = {"nama": "Ali", "usia": 25}
print("Dictionary:", kamus)
#Dictionary menyimpan data dengan pasangan key dan value.

# Set
kumpulan_unik = {1, 2, 3, 3, 4}  # Perhatikan 3 hanya muncul sekali
print("Set:", kumpulan_unik)
#Set memastikan data tidak ada duplikasi.

# NoneType
tidak_ada = None
print("NoneType:", tidak_ada)
#NoneType untuk menyatakan tidak ada nilai.
